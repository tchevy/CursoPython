'''
         STRING - str
 é um tipo de dados primitivo
'''

print('Essa é um string')
print("Essa é um string")
print(123456) #  numero inteiro
print('123456') # string
#print('Essa é uma 'string'(str).') #  errado
print("Essa é uma 'string'(str).") #  correto
print('Essa é uma "string"(str).') #  correto
print("Essa é uma \"string\"(str).") #  correto (caracter de escape  \)
#  usar r na frente da string informa que o texto que está dentro não pode ser executado
print(r'Esse é meu \'Aspas aqui\' (str).')
